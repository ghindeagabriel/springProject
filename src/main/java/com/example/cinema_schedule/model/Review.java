package com.example.cinema_schedule.model;

import javax.persistence.*;

@Entity
@Table(name = "Reviews")
public class Review {
    @Id
    private Long id;

    private String review;

    @ManyToOne
    private Movie movie;

    @ManyToOne
    private User user;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getReview() {
        return review;
    }

    public void setReview(String review) {
        this.review = review;
    }

    public Movie getMovie() {
        return movie;
    }

    public void setMovie(Movie movie) {
        this.movie = movie;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
